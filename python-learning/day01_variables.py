# Day 1: Variables and Data Types
# A variable is a container to store data

# String (text)
name = "Krish"
print(name)

# Integer (whole numbers)
age = 20
print(age)

# Float (decimal numbers)
height = 5.9
print(height)

# Boolean (True or False)
is_student = True
print(is_student)

# Print multiple values
print(name, "is", age, "years old")

