# Day 2: Taking User Input
# Use input() to get data from the user

# Ask the user for their name
name = input("What is your name? ")
print(f"Hello, {name}!")

# Ask for age
age = input("How old are you? ")
print(f"You are {age} years old")

# Note: input() always returns text (string)
# To use it as a number, convert it:
age_number = int(input("Enter your age as a number: "))
print(f"Next year you will be {age_number + 1} years old")

