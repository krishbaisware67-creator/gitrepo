# Day 3: Math Operators
# Do math calculations in Python

# Addition
print(10 + 5)  # 15

# Subtraction
print(10 - 5)  # 5

# Multiplication
print(10 * 5)  # 50

# Division
print(10 / 5)  # 2.0

# Power (exponent)
print(2 ** 3)  # 8 (2 to the power of 3)

# Modulo (remainder)
print(10 % 3)  # 1

# Combine with variables
a = 20
b = 3
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")

