# Day 4: If Statements (Conditionals)
# Make decisions in your code

# Simple if statement
age = 20
if age >= 18:
    print("You are an adult!")

# If-else (two options)
grade = 85
if grade >= 90:
    print("A - Excellent!")
else:
    print("B - Good job!")

# If-elif-else (multiple options)
score = 75
if score >= 90:
    print("Grade A")
elif score >= 80:
    print("Grade B")
elif score >= 70:
    print("Grade C")
else:
    print("Grade F")

